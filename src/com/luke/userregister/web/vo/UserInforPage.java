package com.luke.userregister.web.vo;

public class UserInforPage {
	private int id;
	private String username;
	private String userpws;
	private String userpws2;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUserpws() {
		return userpws;
	}
	public void setUserpws(String userpws) {
		this.userpws = userpws;
	}
	public String getUserpws2() {
		return userpws2;
	}
	public void setUserpws2(String userpws2) {
		this.userpws2 = userpws2;
	}
	
}
